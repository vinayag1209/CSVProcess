package com.csv.process;

public class ZipCodeZone {

	private int zipCode;
	private String zone;
	
	public ZipCodeZone(int zipCode, String zone) {
		this.zipCode = zipCode;
		this.zone = zone;
	}

	public int getZipCode() {
		return zipCode;
	}

	public void setZipCode(int zipCode) {
		this.zipCode = zipCode;
	}

	public String getZone() {
		return zone;
	}

	public void setZone(String zone) {
		this.zone = zone;
	}
	
		
}
